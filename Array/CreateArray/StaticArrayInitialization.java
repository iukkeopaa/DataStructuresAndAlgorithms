package CreateArray;

// 示例代码
public class StaticArrayInitialization {
    public static void main(String[] args) {
        // 声明并初始化一个整型数组
        int[] numbers = {1, 2, 3, 4, 5};
        // 遍历数组并打印元素
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }
    }
}